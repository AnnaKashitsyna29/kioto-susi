/**
 * МОДУЛЬ УВЕДОМЛЕНИЙ
 * 
 * Отвечает за отображение временных уведомлений пользователю.
 * Используется для информирования о действиях (например, "Товар добавлен в корзину").
 * 
 * Особенности:
 * - Уведомление появляется справа вверху экрана
 * - Автоматически исчезает через 2 секунды
 * - Имеет плавную анимацию появления и исчезновения
 * - Стили определены в CSS файле (styles/main.css)
 */

/**
 * ПОКАЗ УВЕДОМЛЕНИЯ ПОЛЬЗОВАТЕЛЮ
 * 
 * Создает временное уведомление, которое автоматически исчезает через 2 секунды.
 * 
 * Процесс работы:
 * 1. Создает новый div элемент для уведомления
 * 2. Присваивает класс 'notification' (стили в CSS)
 * 3. Устанавливает текст сообщения
 * 4. Добавляет элемент в body страницы
 * 5. Через 2 секунды добавляет класс 'notification-hide' для анимации исчезновения
 * 6. Через 300ms (после завершения анимации) удаляет элемент из DOM
 * 
 * @param {string} message - Текст сообщения для отображения
 */
function showNotification(message) {
  // Создаем новый DOM элемент для уведомления
  const notification = document.createElement('div');
  
  // Присваиваем CSS класс для стилизации
  // Класс 'notification' определяет позицию, цвет, размер и анимацию появления
  notification.className = 'notification';
  
  // Устанавливаем текст сообщения
  notification.textContent = message;
  
  // Добавляем элемент в body страницы
  // Уведомление появится в правом верхнем углу благодаря CSS стилям
  document.body.appendChild(notification);
  
  // Устанавливаем таймер для автоматического скрытия уведомления
  setTimeout(() => {
    // Через 2 секунды добавляем класс для анимации исчезновения
    // Класс 'notification-hide' запускает анимацию slideOut
    notification.classList.add('notification-hide');
    
    // Устанавливаем еще один таймер для удаления элемента из DOM
    setTimeout(() => {
      // Проверяем, что элемент все еще существует в DOM
      // (защита от возможных ошибок)
      if (document.body.contains(notification)) {
        // Удаляем элемент из DOM после завершения анимации
        document.body.removeChild(notification);
      }
    }, 300);  // 300ms - время анимации исчезновения (должно совпадать с CSS)
  }, 2000);   // 2000ms (2 секунды) - время отображения уведомления
}

// Делаем функцию доступной глобально для обычных script-скриптов.
window.showNotification = showNotification;
